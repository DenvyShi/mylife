export interface HexagramLine {
  judgment: string;      // 爻辭
  luck: 'yang' | 'yin';  // 老陽(9)、少陽(7) = yang；老陰(6)、少陰(8) = yin
  isChanging: boolean;   // 是否為動爻
}

export interface Hexagram {
  id: number;           // 卦序（1-64）
  name: string;         // 卦名
  symbol: string;       // 卦象符號（☰☷）
  above: string;        // 上卦（外卦）
  below: string;        // 下卦（內卦）
  guaMeaning: string;   // 卦象含義
  judgment: string;     // 卦辭
  judgmentTitle: string; // 彖曰標題
  image: string;         // 象曰
  trigrams: string;     // 上下卦組合含義
  
  // 六爻（從初爻到上爻，index 0 = 初爻，index 5 = 上爻）
  lines: HexagramLine[];
  
  // 變卦信息（當某爻變化時）
  nuclear?: string;     // 之卦（變卦）卦名
  
  fortune: string;       // 總論運勢
  wealth: string;        // 財運
  career: string;        // 事業
  relationships: string; // 感情
  health: string;        // 健康
}

